    public void conector(){
        try{
            con = DriverManager.getConnection(url, user, pass);
            
            if(btnConectarBDD.getText()=="Connect"){
            
                lblEstadoConexion.setText("Successful Connection");
            }
            else{
                lblEstadoConexion.setText("Conexi칩n Exitosa");
            }
            
            lblEstadoConexion.setBackground(Color.green);
            
        }
        catch(SQLException ex){
            lblEstadoConexion.setText("Error: " + ex.getMessage());
            lblEstadoConexion.setBackground(Color.red);
        }
    
    }
